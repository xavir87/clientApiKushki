package com.consumo.api.request;

public class Fitness {
    private String cardio;
    private String rumba;
    private String pool;
    
	public String getCardio() {
		return cardio;
	}
	public void setCardio(String cardio) {
		this.cardio = cardio;
	}
	public String getRumba() {
		return rumba;
	}
	public void setRumba(String rumba) {
		this.rumba = rumba;
	}
	public String getPool() {
		return pool;
	}
	public void setPool(String pool) {
		this.pool = pool;
	}
        
}
